a= 'I am learning \tPython and \nlearning \"new\" technologies'     #\n moves content to next line, \t give space like tab and \"\" write a word in double quotes and etc.
print(a)

print("\U0001F600")
print("Hello\fWorld!")
print("Hello\bWorld!")
# Output:
# HellWorld!

print('It\'s a nice day!')
# Output:
# It's a nice day!

print("This is a backslash: \\")
# Output:
# This is a backslash: \
